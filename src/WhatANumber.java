public class WhatANumber {
    public static void main(String[] args) {
        int x = 6;

        if (x % 2 == 0){
            System.out.println("Число в переменной х равно " + x + " - является чётным");
        }
        else {
            System.out.println("Число в переменной х равно " + x + " - является нечётным");
        }
    }
}
